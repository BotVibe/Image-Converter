async function encodeImage(canvas, ctx, width, height, format, extension, quality) {
    let blob;
    let actualFormat = format;
    let actualExtension = extension;

    // If format is WebP or AVIF AND browser lacks native support, use WASM Encoder!
    if (format === 'image/avif' && !nativeSupport['image/avif']) {
        const imageData = ctx.getImageData(0, 0, width, height);
        const qualityValue = Math.round(quality * 100); // 1 to 100
        const buffer = await encodeAvif(imageData, { quality: qualityValue });
        blob = new Blob([buffer], { type: 'image/avif' });
        actualFormat = 'image/avif';
        actualExtension = 'avif';
    } else if (format === 'image/webp' && !nativeSupport['image/webp']) {
        const imageData = ctx.getImageData(0, 0, width, height);
        const qualityValue = quality * 100;
        const buffer = await encodeWebp(imageData, { quality: qualityValue });
        blob = new Blob([buffer], { type: 'image/webp' });
        actualFormat = 'image/webp';
        actualExtension = 'webp';
    } else if (format === 'image/x-icon') {
        blob = await generateICO(canvas);
        actualFormat = 'image/x-icon';
        actualExtension = 'ico';
    } else {
        // Use Native Browser Encoding
        blob = await new Promise((resolve) => {
            canvas.toBlob(resolve, format, quality);
        });

        // Safety check against silent fail -> fallback to PNG (shouldnt happen anymore but just in case)
        if (blob && format !== blob.type) {
            actualFormat = blob.type;
            actualExtension = actualFormat.split('/')[1];
        } else if (format === 'image/jpeg') {
            // Ensure proper extension for JPEG
            actualExtension = 'jpg';
        }
    }

    return { blob, actualFormat, actualExtension };
}

async function processImage(file, existingId = null) {
    const id = existingId || (Date.now() + Math.random().toString(36).substr(2, 9));

    const isMaxSizeEnabled = document.getElementById('maxFileSizeCheck').checked;

    if (!existingId) {
        originalFiles.set(id, file);
        createResultItem(id, file);
    } else {
        const statusContainer = document.getElementById(`status-${id}`);
        if (isMaxSizeEnabled) {
            statusContainer.innerHTML = `<span class="status-processing" data-i18n="calculating">${i18n[currentLang].calculating}</span>`;
        } else {
            statusContainer.innerHTML = `<span class="status-processing" data-i18n="processing">${i18n[currentLang].processing}</span>`;
        }
    }

    const format = document.getElementById('formatSelect').value;
    const extension = format.split('/')[1];

    // Get quality from slider (used as fallback or initial)
    let baseQuality = parseInt(document.getElementById('qualitySlider').value, 10) / 100;

    // Concurrency control: wait if we're already processing max concurrent images
    if (activeProcessing >= MAX_CONCURRENT) {
        await new Promise(resolve => processingQueue.push(resolve));
    }
    activeProcessing++;

    try {
        // Initialize WASM tools lazily
        await initWasmIfNeeded();

        let img;
        if (imageCache.has(id)) {
            img = imageCache.get(id);
        } else {
            // SVGs cannot be decoded directly from a File object by createImageBitmap in some browsers.
            if (file.type === 'image/svg+xml' || file.name.toLowerCase().endsWith('.svg')) {
                const rawImg = await loadImage(file);
                if (window.createImageBitmap) {
                    img = await createImageBitmap(rawImg);
                } else {
                    img = rawImg;
                }
            } else {
                if (window.createImageBitmap) {
                    try {
                        img = await createImageBitmap(file);
                    } catch (e) {
                        // Fallback in case createImageBitmap fails directly on the file
                        const rawImg = await loadImage(file);
                        img = await createImageBitmap(rawImg);
                    }
                } else {
                    img = await loadImage(file);
                }
            }
            imageCache.set(id, img);
        }

        // Update global max dimensions if this is a new image
        let dimsUpdated = false;
        if (!existingId) {
            if (img.width > globalMaxWidth) { globalMaxWidth = img.width; dimsUpdated = true; }
            if (img.height > globalMaxHeight) { globalMaxHeight = img.height; dimsUpdated = true; }

            if (dimsUpdated) {
                document.getElementById('inputWidth').value = globalMaxWidth;
                document.getElementById('inputHeight').value = globalMaxHeight;
            }
        }

        const { width, height } = calculateDimensions(img.width, img.height);

        const canvas = document.createElement('canvas');
        canvas.width = width;
        canvas.height = height;
        const ctx = canvas.getContext('2d');

        // If target format is JPEG, fill with white background first
        // because JPEG doesn't support transparency and transparent pixels turn black.
        if (format === 'image/jpeg') {
            ctx.fillStyle = '#ffffff';
            ctx.fillRect(0, 0, width, height);
        }

        // Draw image directly to preserve transparency (or over the white bg for jpeg)
        ctx.drawImage(img, 0, 0, width, height);

        // Generate a low-res preview to prevent UI freezing on huge images
        const previewCanvas = document.createElement('canvas');
        const prevCtx = previewCanvas.getContext('2d');
        const MAX_PREV_SIZE = 100;
        const prevRatio = Math.min(MAX_PREV_SIZE / width, MAX_PREV_SIZE / height);
        previewCanvas.width = width * prevRatio;
        previewCanvas.height = height * prevRatio;
        prevCtx.drawImage(img, 0, 0, previewCanvas.width, previewCanvas.height);

        const previewUrl = previewCanvas.toDataURL('image/png', 0.5);
        document.getElementById(`preview-${id}`).src = previewUrl;

        let finalBlob = null;
        let finalFormat = format;
        let finalExtension = extension;

        if (isMaxSizeEnabled) {
            // Update UI to show calculating state if we haven't already
            const statusContainer = document.getElementById(`status-${id}`);
            statusContainer.innerHTML = `<span class="status-processing" data-i18n="calculating">${i18n[currentLang].calculating}</span>`;

            // Calculate target size in Bytes
            const targetVal = parseInt(document.getElementById('targetSizeValueSelect').value, 10);
            const targetUnit = document.getElementById('targetSizeUnitSelect').value;
            const targetSizeBytes = targetUnit === 'MB' ? targetVal * 1024 * 1024 : targetVal * 1024;

            // Unblock UI thread before starting heavy encoding
            await new Promise(resolve => setTimeout(resolve, 0));

            // Strategy: Check if original file is already smaller than target size.
            // If so, attempt to encode at 100% quality (1.0).
            if (file.size <= targetSizeBytes) {
                const { blob: testBlob, actualFormat: testFmt, actualExtension: testExt } = await encodeImage(canvas, ctx, width, height, format, extension, 1.0);

                // Yield to main thread again after first encode attempt
                await new Promise(resolve => setTimeout(resolve, 0));

                if (testBlob && testBlob.size <= targetSizeBytes) {
                    finalBlob = testBlob;
                    finalFormat = testFmt;
                    finalExtension = testExt;
                }
            }

            // If we still don't have a valid blob, do a binary search for the best quality
            if (!finalBlob) {
                let low = 0.01;
                let high = 1.0;
                let bestBlob = null;
                let bestFormat = format;
                let bestExt = extension;
                const maxIterations = 5;

                for (let iter = 0; iter < maxIterations; iter++) {
                    // Update calculating text occasionally if possible, yield to UI thread
                    await new Promise(resolve => setTimeout(resolve, 0));

                    const mid = (low + high) / 2;
                    const { blob: iterBlob, actualFormat: iterFmt, actualExtension: iterExt } = await encodeImage(canvas, ctx, width, height, format, extension, mid);

                    if (!iterBlob) break;

                    if (iterBlob.size <= targetSizeBytes) {
                        bestBlob = iterBlob;
                        bestFormat = iterFmt;
                        bestExt = iterExt;
                        low = mid; // Try to go higher to get better quality
                    } else {
                        high = mid; // Size is too big, go lower
                    }
                }

                if (bestBlob) {
                    finalBlob = bestBlob;
                    finalFormat = bestFormat;
                    finalExtension = bestExt;
                } else {
                    // Yield to UI before fallback encode
                    await new Promise(resolve => setTimeout(resolve, 0));
                    // Fallback to lowest possible quality if we couldn't hit the target
                    const { blob: fallbackBlob, actualFormat: fallFmt, actualExtension: fallExt } = await encodeImage(canvas, ctx, width, height, format, extension, 0.01);
                    finalBlob = fallbackBlob;
                    finalFormat = fallFmt;
                    finalExtension = fallExt;
                }
            }

        } else {
            // Normal slider-based encoding
            // Unblock UI thread before starting heavy encoding
            await new Promise(resolve => setTimeout(resolve, 0));
            const { blob, actualFormat, actualExtension } = await encodeImage(canvas, ctx, width, height, format, extension, baseQuality);
            finalBlob = blob;
            finalFormat = actualFormat;
            finalExtension = actualExtension;
        }

        if (!finalBlob) {
            showError(id);
            return;
        }

        const safeBaseName = sanitizeFilename(file.name).replace(/\.[^/.]+$/, "");
        const newName = safeBaseName + `.${finalExtension}`;

        // Store for ZIP
        const existingIndex = convertedFiles.findIndex(f => f.id === id);
        if (existingIndex > -1) {
            convertedFiles.splice(existingIndex, 1);
        }
        convertedFiles.push({ id: id, name: newName, blob: finalBlob });

        const blobUrl = URL.createObjectURL(finalBlob);

        // Format sizes nicely
        const origSizeMB = (file.size / (1024 * 1024)).toFixed(2);
        let compSizeStr = '';
        if (finalBlob.size > 1024 * 1024) {
            compSizeStr = (finalBlob.size / (1024 * 1024)).toFixed(2) + ' MB';
        } else {
            compSizeStr = (finalBlob.size / 1024).toFixed(1) + ' KB';
        }

        document.getElementById(`meta-${id}`).innerHTML = `
            ${width}x${height}px •
            <span data-i18n="original">${i18n[currentLang].original}</span>: ${origSizeMB} MB
            &rarr;
            <span data-i18n="compressed">${i18n[currentLang].compressed}</span>: ${compSizeStr}
        `;

        const statusContainer = document.getElementById(`status-${id}`);

        // Display normal success state regardless of whether WASM polyfill was used
        statusContainer.innerHTML = `
            <div class="action-buttons-row">
                <a href="${blobUrl}" download="${escapeHTML(newName)}" class="btn primary" style="text-decoration:none;">
                    <span data-i18n="download">${i18n[currentLang].download}</span>
                </a>
                <button class="btn delete-btn" title="${i18n[currentLang].remove}" onclick="removeResult('${id}')">
                    ${DELETE_ICON_SVG}
                </button>
            </div>
        `;

    } catch (e) {
        showError(id);
        console.error("Error processing image:", e);
    } finally {
        // Release concurrency slot
        activeProcessing--;
        if (processingQueue.length > 0) {
            // Unblock the next waiting processImage call
            const nextResolve = processingQueue.shift();
            nextResolve();
        }
    }
}

async function generateICO(canvas) {
    return new Promise((resolve) => {
        const width = canvas.width;
        const height = canvas.height;

        // ICO requires images to be <= 256x256.
        // Preserve aspect ratio when scaling down to fit within 256x256.
        let w = width;
        let h = height;
        if (w > 256 || h > 256) {
            const ratio = Math.min(256 / w, 256 / h);
            w = Math.round(w * ratio);
            h = Math.round(h * ratio);
        }

        // Create a temporary canvas to resize if necessary and ensure correct dimensions
        const tmpCanvas = document.createElement('canvas');
        tmpCanvas.width = w;
        tmpCanvas.height = h;
        const tmpCtx = tmpCanvas.getContext('2d');
        tmpCtx.drawImage(canvas, 0, 0, w, h);

        const imgData = tmpCtx.getImageData(0, 0, w, h);
        const data = imgData.data;

        // ICO Header (6 bytes)
        // 0-1: Reserved (0)
        // 2-3: Type (1 for ICO)
        // 4-5: Number of images (1)

        // Image Directory (16 bytes)
        // 0: Width (0 means 256)
        // 1: Height (0 means 256)
        // 2: Color count (0 for 32-bit)
        // 3: Reserved (0)
        // 4-5: Color planes (1)
        // 6-7: Bit count (32)
        // 8-11: Size of image data in bytes
        // 12-15: Offset of image data from beginning of file (22)

        // BITMAPINFOHEADER (40 bytes)
        // ...

        const bytesPerPixel = 4;
        const xorMaskSize = w * h * bytesPerPixel;
        // AND mask size must be padded to a multiple of 4 bytes per row
        const rowBytes = Math.ceil(w / 32) * 4;
        const andMaskSize = rowBytes * h;
        const infoHeaderSize = 40;
        const imageSize = infoHeaderSize + xorMaskSize + andMaskSize;
        const fileSize = 6 + 16 + imageSize;

        const buffer = new ArrayBuffer(fileSize);
        const view = new DataView(buffer);
        const uint8 = new Uint8Array(buffer);

        // ICO Header
        view.setUint16(0, 0, true); // Reserved
        view.setUint16(2, 1, true); // Type: 1
        view.setUint16(4, 1, true); // Image count

        // Image Directory
        view.setUint8(6, w === 256 ? 0 : w); // Width
        view.setUint8(7, h === 256 ? 0 : h); // Height
        view.setUint8(8, 0); // Colors
        view.setUint8(9, 0); // Reserved
        view.setUint16(10, 1, true); // Planes
        view.setUint16(12, 32, true); // BPP
        view.setUint32(14, imageSize, true); // Size
        view.setUint32(18, 22, true); // Offset

        // DIB Header (BITMAPINFOHEADER)
        view.setUint32(22, infoHeaderSize, true); // Header size
        view.setInt32(26, w, true); // Width
        view.setInt32(30, h * 2, true); // Height (x2 for XOR and AND masks)
        view.setUint16(34, 1, true); // Planes
        view.setUint16(36, 32, true); // BPP
        view.setUint32(38, 0, true); // Compression (0 = BI_RGB)
        view.setUint32(42, xorMaskSize + andMaskSize, true); // Image size
        view.setInt32(46, 0, true); // X pixels per meter
        view.setInt32(50, 0, true); // Y pixels per meter
        view.setUint32(54, 0, true); // Colors used
        view.setUint32(58, 0, true); // Important colors

        // Pixel Data (XOR Mask) - Bottom-up, BGRA order
        let offset = 22 + infoHeaderSize;
        for (let y = h - 1; y >= 0; y--) {
            for (let x = 0; x < w; x++) {
                const i = (y * w + x) * 4;
                view.setUint8(offset++, data[i + 2]); // B
                view.setUint8(offset++, data[i + 1]); // G
                view.setUint8(offset++, data[i + 0]); // R
                view.setUint8(offset++, data[i + 3]); // A
            }
        }

        // AND Mask (1 bit per pixel, padded to 32 bits per row) - Bottom-up
        // Since we are using 32-bit alpha in XOR mask, AND mask can be all 0s (opaque)
        // to let the 8-bit alpha channel handle transparency.
        // We initialize it to 0 implicitly when ArrayBuffer is created.

        resolve(new Blob([buffer], { type: 'image/x-icon' }));
    });
}

function loadImage(file) {
    return new Promise((resolve, reject) => {
        const url = URL.createObjectURL(file);
        const img = new Image();
        img.onload = () => {
            URL.revokeObjectURL(url);
            resolve(img);
        };
        img.onerror = () => {
            URL.revokeObjectURL(url);
            reject(new Error("Failed to load image"));
        };
        img.src = url;
    });
}

function showError(id) {
    const statusContainer = document.getElementById(`status-${id}`);
    statusContainer.innerHTML = `<span class="status-error" data-i18n="error">${i18n[currentLang].error}</span>`;
}


window.removeResult = function(id) {
    const li = document.getElementById(`item-${id}`);
    if (!li) return;

    // Revoke object URLs to prevent memory leaks
    const preview = li.querySelector('.result-preview');
    if (preview && preview.src.startsWith('blob:')) {
        URL.revokeObjectURL(preview.src);
    }
    const download = li.querySelector('a[download]');
    if (download && download.href.startsWith('blob:')) {
        URL.revokeObjectURL(download.href);
    }

    li.remove();
    originalFiles.delete(id);

    // Release cached image
    if (imageCache.has(id)) {
        const cachedImg = imageCache.get(id);
        if (cachedImg.close) cachedImg.close(); // Close ImageBitmap
        imageCache.delete(id);
    }

    const index = convertedFiles.findIndex(f => f.id === id);
    if (index > -1) convertedFiles.splice(index, 1);

    if (convertedFiles.length === 0) {
        document.getElementById('resultsPanel').classList.add('hidden');
        // Reset global dimensions when all items are removed
        globalMaxWidth = 0;
        globalMaxHeight = 0;
        document.getElementById('inputWidth').value = '';
        document.getElementById('inputHeight').value = '';
    }
};

function clearAll() {
    // Revoke object URLs to prevent memory leaks
    const links = document.querySelectorAll('#resultsList a[download]');
    links.forEach(link => {
        if (link.href.startsWith('blob:')) {
            URL.revokeObjectURL(link.href);
        }
    });

    const previews = document.querySelectorAll('#resultsList img.result-preview');
    previews.forEach(img => {
        if (img.src.startsWith('blob:')) {
            URL.revokeObjectURL(img.src);
        }
    });

    document.getElementById('resultsList').innerHTML = '';
    document.getElementById('resultsPanel').classList.add('hidden');
    convertedFiles.length = 0;
    originalFiles.clear();

    // Reset global max dimensions
    globalMaxWidth = 0;
    globalMaxHeight = 0;
    document.getElementById('inputWidth').value = '';
    document.getElementById('inputHeight').value = '';

    // Release all cached images
    for (const cachedImg of imageCache.values()) {
        if (cachedImg.close) cachedImg.close();
    }
    imageCache.clear();
}


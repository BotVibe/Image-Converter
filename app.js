function setupFormLimits() {
    const formatSelect = document.getElementById('formatSelect');
    const inputWidth = document.getElementById('inputWidth');
    const inputHeight = document.getElementById('inputHeight');

    const enforceLimits = () => {
        // We no longer auto-set to MAX_IMAGE_DIMENSION if empty, to allow the actual image dimensions to persist
        // We only enforce the clamp on max MAX_IMAGE_DIMENSION.
        if (inputWidth.value && parseInt(inputWidth.value) > MAX_IMAGE_DIMENSION) {
            inputWidth.value = MAX_IMAGE_DIMENSION;
        }
        if (inputHeight.value && parseInt(inputHeight.value) > MAX_IMAGE_DIMENSION) {
            inputHeight.value = MAX_IMAGE_DIMENSION;
        }
    };

    const clampLimits = () => {
        if (inputWidth.value && parseInt(inputWidth.value) > MAX_IMAGE_DIMENSION) inputWidth.value = MAX_IMAGE_DIMENSION;
        if (inputHeight.value && parseInt(inputHeight.value) > MAX_IMAGE_DIMENSION) inputHeight.value = MAX_IMAGE_DIMENSION;
    };

    formatSelect.addEventListener('change', enforceLimits);
    inputWidth.addEventListener('change', enforceLimits);
    inputHeight.addEventListener('change', enforceLimits);
    inputWidth.addEventListener('input', clampLimits);
    inputHeight.addEventListener('input', clampLimits);
}

function setupQualityAndFormat() {
    const formatSelect = document.getElementById('formatSelect');
    const inputWidth = document.getElementById('inputWidth');
    const inputHeight = document.getElementById('inputHeight');
    const qualitySlider = document.getElementById('qualitySlider');
    const qualityValue = document.getElementById('qualityValue');
    const sliderContainer = qualitySlider.parentElement;

    const qualityGroup = document.getElementById('qualityGroup');
    const maxSizeGroup = document.getElementById('maxSizeGroup');
    const maxFileSizeCheck = document.getElementById('maxFileSizeCheck');
    const targetSizeValueSelect = document.getElementById('targetSizeValueSelect');
    const targetSizeUnitSelect = document.getElementById('targetSizeUnitSelect');

    const updateMaxSizeOptions = () => {
        const unit = targetSizeUnitSelect.value;
        targetSizeValueSelect.innerHTML = '';
        if (unit === 'MB') {
            for (let i = 1; i <= 10; i++) {
                const opt = document.createElement('option');
                opt.value = i;
                opt.textContent = i;
                targetSizeValueSelect.appendChild(opt);
            }
        } else {
            for (let i = 100; i <= 1000; i += 100) {
                const opt = document.createElement('option');
                opt.value = i;
                opt.textContent = i;
                targetSizeValueSelect.appendChild(opt);
            }
        }

        // Re-initialize custom select for this specific dropdown if it exists,
        // or let the initial setup catch it.
        const existingWrapper = targetSizeValueSelect.closest('.custom-select-wrapper');
        if (existingWrapper) {
            // Un-wrap and re-wrap to recreate options
            existingWrapper.parentNode.insertBefore(targetSizeValueSelect, existingWrapper);
            existingWrapper.remove();
            setupCustomSelects(); // Will only process un-wrapped ones
        }
    };

    targetSizeUnitSelect.addEventListener('change', () => {
        updateMaxSizeOptions();
        triggerRecompress();
    });

    const toggleMaxSizeUI = () => {
        // Dynamically fetch elements to prevent stale references if DOM changes
        const qGroup = document.getElementById('qualityGroup');
        const mGroup = document.getElementById('maxSizeGroup');
        const check = document.getElementById('maxFileSizeCheck');

        if (check && qGroup && mGroup) {
            if (check.checked) {
                qGroup.classList.add('hidden');
                mGroup.classList.remove('hidden');
            } else {
                qGroup.classList.remove('hidden');
                mGroup.classList.add('hidden');
            }
        }
    };

    maxFileSizeCheck.addEventListener('change', () => {
        toggleMaxSizeUI();
        triggerRecompress();
    });

    // Also bind to click to handle iOS Safari and other edge cases where change might be swallowed
    maxFileSizeCheck.addEventListener('click', () => {
        // Just let it toggle the native checked state, the change event or this will handle UI
        setTimeout(() => {
            toggleMaxSizeUI();
        }, 0);
    });

    targetSizeValueSelect.addEventListener('change', triggerRecompress);

    // Initial setup
    updateMaxSizeOptions();

    const updateSliderPercent = () => {
        const val = qualitySlider.value;
        const min = qualitySlider.min || 1;
        const max = qualitySlider.max || 100;
        const percent = ((val - min) / (max - min)) * 100;
        qualitySlider.style.setProperty('--slider-percent', `${percent}%`);
        return percent;
    };
    updateSliderPercent();

    // Quality slider text update and animations
    qualitySlider.addEventListener('input', (e) => {
        qualityValue.textContent = e.target.value;
        const percent = updateSliderPercent();

        // Spawn comic star
        const star = document.createElement('div');
        star.className = 'comic-star';

        // Calculate thumb position for star origin
        const thumbWidth = 28;
        const offset = (percent / 100) * (qualitySlider.clientWidth - thumbWidth) + (thumbWidth / 2);

        star.style.left = `${offset - 10}px`; // center star
        star.style.top = '0px';

        // Randomize spill direction
        const dir = Math.random() > 0.5 ? 1 : -1;
        const spillX = dir * (10 + Math.random() * 20) + 'px';
        const spillRot = (dir * (45 + Math.random() * 90)) + 'deg';

        star.style.setProperty('--spill-x', spillX);
        star.style.setProperty('--spill-rot', spillRot);

        sliderContainer.appendChild(star);

        // Clean up star after animation (3s)
        setTimeout(() => {
            if (star.parentNode) {
                star.parentNode.removeChild(star);
            }
        }, 3000);
    });

    qualitySlider.addEventListener('change', triggerRecompress);
    formatSelect.addEventListener('change', triggerRecompress);
    inputWidth.addEventListener('change', triggerRecompress);
    inputHeight.addEventListener('change', triggerRecompress);
}

function setupDragAndDrop() {
    const dropZone = document.getElementById('dropZone');
    const fileInput = document.getElementById('fileInput');

    dropZone.addEventListener('click', () => fileInput.click());

    dropZone.addEventListener('dragover', (e) => {
        e.preventDefault();
        dropZone.classList.add('dragover');
    });

    dropZone.addEventListener('dragleave', (e) => {
        e.preventDefault();
        dropZone.classList.remove('dragover');
    });

    dropZone.addEventListener('drop', (e) => {
        e.preventDefault();
        dropZone.classList.remove('dragover');
        if (e.dataTransfer.files && e.dataTransfer.files.length > 0) {
            handleFiles(e.dataTransfer.files);
        }
    });

    fileInput.addEventListener('change', (e) => {
        if (e.target.files && e.target.files.length > 0) {
            handleFiles(e.target.files);
        }
        // Reset so the same file can be selected again
        fileInput.value = '';
    });
}

